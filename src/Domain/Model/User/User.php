<?php
namespace Lavegui\Calendar\Domain\Model\User;

/**
 * Class User
 * @package Lavegui\Calendar\Domain\Model\User
 */
class User
{
    /** @var int */
    private $id;
    /** @var string */
    private $username;
    /** @var string */
    private $email;

    /**
     * @return string
     */
    public function getUsername()
    {
        return $this->username;
    }

    /**
     * @param string $username
     */
    public function setUsername($username)
    {
        $this->username = $username;
    }

    /**
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param int $id
     */
    public function setId($id)
    {
        $this->id = $id;
    }

    /**
     * @return string
     */
    public function getEmail()
    {
        return $this->email;
    }

    /**
     * @param string $email
     */
    public function setEmail($email)
    {
        $this->email = $email;
    }
}
